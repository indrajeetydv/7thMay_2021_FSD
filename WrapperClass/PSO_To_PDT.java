public class PSO_To_PDT{
	public static void main(String[] args){
		//
		int x=Integer.parseInt("200");

		//PSO->WCO->PDT
		int i=Integer.valueOf("201").intValue();

		System.out.println("x: "+x);//10
		System.out.println("i: "+i);//10

		//
		float f=Float.parseFloat("123.43f");
		double d=Double.parseDouble("123.43");

		//
		Boolean b=Boolean.parseBoolean("true");
		Byte b1=Byte.parseByte("40");


		System.out.println("f: "+f);//10
		System.out.println("d: "+d);//10
		System.out.println("b: "+b);//10
		System.out.println("b1: "+b1);//10

	}
}